async function seedBusiness() {
  try {
    await mongoose.connect(process.env.MONGODB_URI);
    console.log("Connected to MongoDB");

    // Get current business page
    const page = await Page.findOne({ slug: "business" });
    if (!page) {
      console.log("Business page not found. Creating it...");
      await Page.create({
        slug: "business",
        name: "Business",
        path: "/business",
        sections: {
          solutions: DEFAULT_BUSINESS_SOLUTIONS,
        },
        published: true,
      });
      console.log("✅ Business page created with 12 solution cards");
    } else {
      // Update only the solutions section while preserving other sections
      await Page.updateOne(
        { slug: "business" },
        { $set: { "sections.solutions": DEFAULT_BUSINESS_SOLUTIONS } }
      );
      console.log("✅ Business page updated with 12 solution cards");
    }

    await mongoose.connection.close();
    console.log("Database connection closed");
  } catch (error) {
    console.error("Seeding failed:", error);
    process.exit(1);
  }
}

seedBusiness();
